package vo;

public class ImageVO {
	private String img_idx, p_idx, ori_name, file_name, file_size, img_date, img_status;

	public String getImg_idx() {
		return img_idx;
	}

	public void setImg_idx(String img_idx) {
		this.img_idx = img_idx;
	}

	public String getP_idx() {
		return p_idx;
	}

	public void setP_idx(String p_idx) {
		this.p_idx = p_idx;
	}

	public String getOri_name() {
		return ori_name;
	}

	public void setOri_name(String ori_name) {
		this.ori_name = ori_name;
	}

	public String getFile_name() {
		return file_name;
	}

	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}

	public String getFile_size() {
		return file_size;
	}

	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}

	public String getImg_date() {
		return img_date;
	}

	public void setImg_date(String img_date) {
		this.img_date = img_date;
	}

	public String getImg_status() {
		return img_status;
	}

	public void setImg_status(String img_status) {
		this.img_status = img_status;
	}
	
}
